	private void writeJSON(RootObject product, String fileName)
			throws JsonGenerationException, JsonMappingException, IOException {
		String GLNDirName = fileName.split("_")[0];
		File GLNDirectory = new File(destinationDirectory+GLNDirName);
		GLNDirectory.mkdir();
		ObjectMapper mapper = new ObjectMapper();
		mapper.writeValue(new File(destinationDirectory+GLNDirName+"/"+fileName), product);
	}


